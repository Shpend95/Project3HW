package org.example.class1;

public class E5Variables {
    public static void main(String[] args) {
        //create a box and store 18 inside it
        int days=18;
        System.out.println(days);
        //update the value of days to 19
        days=19;
        System.out.println(days);

        int age=20;
        System.out.println(age);
        age=21;
        System.out.println(age);







    }
}
